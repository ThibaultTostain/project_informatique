package main;

public class Force {

	private double x;
	private double y;
	
	// Constructeurs
	
	public Force(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	// Get & Set
	
	public double getX() {
		return x;
	}
	
	
	public void setX(double x) {
		this.x = x;
	}
	
	public double getY() {
		return y;
	}
	
	public void setY(double y) {
		this.y = y;
	}

}
